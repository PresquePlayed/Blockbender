	public void generate(Random random, int chunkX, int chunkZ, World world, IChunkProvider chunkGenerator, IChunkProvider chunkProvider) {
		int[] result = GenerationUtils.shouldSpawnHere(random, chunkX, chunkZ, world, 20, 2, 25, biomes);
		if(result[0] == 0){
			this.generate(world, result[1], result[2] - 4, result[3]);
			this.generateEntities(world, result[1], result[2] - 4, result[3], random);
			this.generateLoot(world, result[1], result[2] - 4, result[3], random);
		}
	}
	
	public void generateLoot(World world, int i, int j, int k, Random random){
		for(int x = i; x < i + 20; x++){
			for (int z = k; z < k + 20; z++) {
				for (int y = j; y < j + 5; y++) {
						TileEntity e = world.getTileEntity(x, y, z);
						if(e != null && e instanceof TileEntityChest){
							GenerationUtils.generateScrolls(random, (TileEntityChest) e, ElementManager.WATER, 1);
							GenerationUtils.generateRandomMoney(random, (TileEntityChest) e, ElementManager.WATER, 2);
							int ran = random.nextInt(10);
							for(int r = 0; r < random.nextInt(3); r++){
								switch (ran){
								case 1:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.SeaPruneItem, random.nextInt(5), 0));	
									break;
								case 2:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.SeaSquidItem, random.nextInt(5), 0));	
									break;
								case 3:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.SeaSquidSoupItem, 1, 0));	
									break;
								case 4:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.SeaweedBreadItem, random.nextInt(5), 0));	
									break;
								case 5:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.SeaweedSoupItem, 1, 0));	
									break;
								}	
							}
							
						}
				}
			}
		}
	}
	
	public void generateEntities(World world, int i, int j, int k, Random random) {
		int numM = random.nextInt(3) + 1;
		int numW = random.nextInt(3) + 1;

		for (int x = 0; x < numM; x++) {
			EntityWaterMan waterMan = new EntityWaterMan(world, i + 10, j + 7, k + 10, 9);
			waterMan.setPositionAndRotation(i + 10, j + 7, k + 10, 1.0F, 1.0F);
			world.spawnEntityInWorld(waterMan);
		}

		for (int x = 0; x < numW; x++) {
			EntityWaterWoman waterWoman = new EntityWaterWoman(world, i + 10, j + 7, k + 10, 9);
			waterWoman.setPositionAndRotation(i + 10, j + 7, k + 10, 1.0F, 1.0F);
			world.spawnEntityInWorld(waterWoman);
		}

		EntityPolarBearDog polarDog = new EntityPolarBearDog(world);
		polarDog.setPositionAndRotation(i + 30, j + 7, k + 30, 1.0F, 1.0F);
		world.spawnEntityInWorld(polarDog);

	}

