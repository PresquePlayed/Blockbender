	public void generate(Random random, int chunkX, int chunkZ, World world, IChunkProvider chunkGenerator, IChunkProvider chunkProvider) {
		int[] result = GenerationUtils.shouldSpawnHere(random, chunkX, chunkZ, world, 12, 2, 35, biomes);
		if(result[0] == 0){
			this.masterGenerate(world, result[1], result[2] - 3, result[3]);
			this.generateEntities(world, result[1], result[2] - 3, result[3], random);
			this.generateLoot(world, result[1], result[2] - 3, result[3], random);
		}
	}
	
	public void generateLoot(World world, int i, int j, int k, Random random){
		for(int x = i; x < i + 12; x++){
			for (int z = k; z < k + 12; z++) {
				for (int y = j; y < j + 25; y++) {
						TileEntity e = world.getTileEntity(x, y, z);
						if(e != null && e instanceof TileEntityChest){
							GenerationUtils.generateScrolls(random, (TileEntityChest) e, ElementManager.WATER, 3);
							GenerationUtils.generateRandomMoney(random, (TileEntityChest) e, ElementManager.WATER, 1);
						}
				}
			}
		}
	}

	
	public void generateEntities(World world, int i, int j, int k, Random random) {
		int numM = random.nextInt(2) + 2;

		for (int x = 0; x < numM; x++) {
			EntityWaterMan waterMan = new EntityWaterMan(world, i + 6, j + 5, k + 6, 9);
			waterMan.setPositionAndRotation(i + 7, j + 5, k + 7, 1.0F, 1.0F);
			world.spawnEntityInWorld(waterMan);
		}
	}

