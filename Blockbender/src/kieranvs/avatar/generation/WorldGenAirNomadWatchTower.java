	@Override
	public void generate(Random random, int chunkX, int chunkZ, World world, IChunkProvider chunkGenerator, IChunkProvider chunkProvider) {
		int[] result = GenerationUtils.shouldSpawnHere(random, chunkX, chunkZ, world, 10, 5, 40, biomes);
		if(result[0] == 0){
			this.masterGenerate(world, result[1], result[2] - 5, result[3]);
			this.generateEntities(world, result[1], result[2] - 5, result[3], random);
			this.generateLoot(world, result[1], result[2] - 5, result[3], random);
		}
	}
	
	public boolean masterGenerate(World world, int i, int j, int k) {
		
		generate(world, i, j, k);

		return true;
	}

	public void generateEntities(World world, int i, int j, int k, Random random) {
		int numM = random.nextInt(2) + 1;

		for (int x = 0; x < numM; x++) {
			EntityAirMan airMan = new EntityAirMan(world, i + 5, j + 7, k + 5, 5);
			airMan.setPositionAndRotation(i + 7, j + 7, k + 7, 1.0F, 1.0F);
			world.spawnEntityInWorld(airMan);
		}

	}	

	public void generateLoot(World world, int i, int j, int k, Random random){
		for(int x = i; x < i + 10; x++){
			for (int z = k; z < k + 10; z++) {
				for (int y = j; y < j + 25; y++) {
						TileEntity e = world.getTileEntity(x, y, z);
						if(e != null && e instanceof TileEntityChest){
							GenerationUtils.generateScrolls(random, (TileEntityChest) e, ElementManager.AIR, 3);
							int ran = random.nextInt(10);
							for(int r = 0; r < random.nextInt(3); r++){
								switch (ran){
								case 0:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(GenerationUtils.gliders.get(random.nextInt(GenerationUtils.gliders.size())), 1, 0));
									break;
								case 1:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.CustardTartItem, random.nextInt(2), 0));
									break;
								case 2:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.FruitPieItem, random.nextInt(5), 0));	
									break;
								case 3:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceGrainItem, random.nextInt(32), 0));
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(GenerationUtils.gliders.get(random.nextInt(GenerationUtils.gliders.size())), 1, 0));
									break;
								case 4:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceGrainItem, random.nextInt(32), 0));	
									break;
								case 5:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceBowlItem, 1, 0));
									break;
								case 6:
									((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(GenerationUtils.gliders.get(random.nextInt(GenerationUtils.gliders.size())), 1, 0));
									break;
								}	
							}
							
						}
				}
			}
		}
	}
	
