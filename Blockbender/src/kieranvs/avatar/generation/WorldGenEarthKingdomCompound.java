	@Override
	public void generate(Random random, int chunkX, int chunkZ, World world, IChunkProvider chunkGenerator, IChunkProvider chunkProvider) {
		int[] result = GenerationUtils.shouldSpawnHere(random, chunkX, chunkZ, world, 23, 2, 40, biomes);
		if(result[0] == 0){
			this.masterGenerate(world, result[1], result[2], result[3]);
			this.generateLoot(world, result[1], result[2], result[3], random);
			this.generateEntities(world, result[1], result[2], result[3], random);
		}

	}

	public void generateEntities(World world, int i, int j, int k, Random random) {
		int numM = random.nextInt(2) + 1;
		int numW = random.nextInt(2) + 1;
		int numP = random.nextInt(4) + 2;
		int[] yCoord = new int[2];
		yCoord[0] = 2;
		yCoord[1] = 6;

		for (int x = 0; x < numP; x++) {
			EntityPig pig = new EntityPig(world);
			world.spawnEntityInWorld(pig);
		}

		for (int x = 0; x < numM; x++) {
			EntityEarthMan earthMan = new EntityEarthMan(world, i + 12, j + 1, k + 12, 11);
			earthMan.setPositionAndRotation(i + 5, j + yCoord[random.nextInt(1)], k + 5, 1.0F, 1.0F);
			world.spawnEntityInWorld(earthMan);
		}

		for (int x = 0; x < numW; x++) {
			EntityEarthWoman earthWoman = new EntityEarthWoman(world, i + 12, j + 1, k + 12, 11);
			earthWoman.setPositionAndRotation(i + 19, j + 2, k + 19, 1.0F, 1.0F);
			world.spawnEntityInWorld(earthWoman);
		}

	}	

	public void generateLoot(World world, int i, int j, int k, Random random){
		for(int x = i; x < i + 23; x++){
			for (int z = k; z < k + 23; z++) {
				for (int y = j; y < j + 13; y++) {
					TileEntity e = world.getTileEntity(x, y, z);
					if(e != null && e instanceof TileEntityChest){
						GenerationUtils.generateScrolls(random, (TileEntityChest) e, ElementManager.EARTH, 1);
						GenerationUtils.generateRandomMoney(random, (TileEntityChest) e, ElementManager.EARTH, 2);
						int ran = random.nextInt(10);
						for(int r = 0; r < random.nextInt(3); r++){
							switch (ran){
							case 1:
								((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceGrainItem, random.nextInt(32), 0));	
								break;
							case 2:
								((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceGrainItem, random.nextInt(32), 0));	
								break;
							case 3:
								((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceBowlItem, 1, 0));	
								break;
							}	
						}

					}
				}
			}
		}
	}

