	private static BiomeGenBase[] biomes = {BiomeGenBase.desert, BiomeGenBase.desertHills, BiomeGenBase.savanna, BiomeGenBase.savannaPlateau};
	
	@Override
	public void generate(Random random, int chunkX, int chunkZ, World world, IChunkProvider chunkGenerator, IChunkProvider chunkProvider) {
		int[] result = GenerationUtils.shouldSpawnHere(random, chunkX, chunkZ, world, 15, 2, 50, biomes);
		if(result[0] == 0){
			this.masterGenerate(world, result[1], result[2], result[3]);
			this.generateEntities(world, result[1], result[2] - 5, result[3], random);
			this.generateLoot(world, result[1], result[2], result[3], random);
		}
		
	}
	
	public void generateLoot(World world, int i, int j, int k, Random random){
		for(int x = i; x < i + 15; x++){
			for (int z = k; z < k + 15; z++) {
				for (int y = j; y < j + 12; y++) {
					TileEntity e = world.getTileEntity(x, y, z);
					if(e != null && e instanceof TileEntityChest){
						GenerationUtils.generateScrolls(random, (TileEntityChest) e, ElementManager.FIRE, 2);
						GenerationUtils.generateRandomMoney(random, (TileEntityChest) e, ElementManager.FIRE, 3);
						int ran = random.nextInt(10);
						for(int r = 0; r < random.nextInt(3); r++){
							switch (ran){
							case 1:
								((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceGrainItem, random.nextInt(32), 0));	
								break;
							case 2:
								((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceGrainItem, random.nextInt(32), 0));	
								break;
							case 3:
								((TileEntityChest) e).setInventorySlotContents(random.nextInt(27), new ItemStack(mod_Avatar.RiceBowlItem, 1, 0));	
								break;
							}	
						}

					}
				}
			}
		}
	}
	
	public void generateEntities(World world, int i, int j, int k, Random random) {
		int numM = random.nextInt(3) + 1;
		int numW = random.nextInt(3) + 1;

		for (int x = 0; x < numM; x++) {
			EntityFireMan FireMan = new EntityFireMan(world, i + 16, j + 7, k + 16, 9);
			FireMan.setPositionAndRotation(i + 16, j + 7, k + 16, 1.0F, 1.0F);
			world.spawnEntityInWorld(FireMan);
		}

		for (int x = 0; x < numW; x++) {
			EntityFireWoman FireWoman = new EntityFireWoman(world, i + 16, j + 7, k + 16, 9);
			FireWoman.setPositionAndRotation(i + 16, j + 7, k + 16, 1.0F, 1.0F);
			world.spawnEntityInWorld(FireWoman);
		}
	}
	
